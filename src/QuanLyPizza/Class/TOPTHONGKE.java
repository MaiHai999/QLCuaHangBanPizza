public class TOPTHONGKE {

    private String TopTenSp;
    private int TopSoLuong;

    public TOPTHONGKE() {
    }

    public TOPTHONGKE(String TopTenSp, int TopSoLuong) {
        this.TopTenSp = TopTenSp;
        this.TopSoLuong = TopSoLuong;
    }

    public String getTopTenSp() {
        return TopTenSp;
    }

    public int getTopSoLuong() {
        return TopSoLuong;
    }

    public void setTopTenSp(String TopTenSp) {
        this.TopTenSp = TopTenSp;
    }

    public void setTopSoLuong(int TopSoLuong) {
        this.TopSoLuong = TopSoLuong;
    }


}
